    public void buscar(){
            
         String codigo = jTextFieldCodigo.getText();
         
        if(jTextFieldCodigo.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Debe ingresar el cÃ³digo en la caja de texto");
            jTextFieldCodigo.requestFocus();
        }else{
            
            ep = pDAO.buscar(codigo);
            if (ep.getCodigo()!= null ){
                jTextFieldId.setText(String.valueOf(ep.getIdProducto()));
                jTextFieldNombre.setText(ep.getNombre());
                jTextFieldPrecio.setText(String.valueOf(ep.getPrecioVenta()));
                jTextFieldCosto.setText(String.valueOf(ep.getCosto()));
                
                if (ep.getEstadoProducto()== 1){  
                jTextFieldEstado.setText("Disponible");
                }
                if(ep.getEstadoProducto()==2){
                jTextFieldEstado.setText("No Disponible");
                }
                
                jButtonHabilitar.setEnabled(true);
                jButtonDeshabilitar.setEnabled(true);
                jButtonBuscar.setEnabled(false);
                jTextFieldCodigo.setEditable(false);
                
            }else{
                 JOptionPane.showMessageDialog(this,"El cÃ³digo que ingreso no estÃ¡ registrado");
                 jTextFieldCodigo.requestFocus();
             }
        } 
    }
