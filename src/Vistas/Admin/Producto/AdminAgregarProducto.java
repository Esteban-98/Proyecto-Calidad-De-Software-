    public void generarCodigoProducto(){
    
        String idProducto = pDAO.obtenerIdProducto();
        int idPro;
        if (idProducto.equals("")){
            idPro=0;
        }else{
            idPro = Integer.parseInt(idProducto);
        }
        int incremento = idPro + 1;
        String codigoGenerado = "R"+incremento;
    
        jTextFieldCodigo.setText(codigoGenerado);
    
    }
    
    private void jButtonAgregarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarUsuarioActionPerformed

        String codigoP = jTextFieldCodigo.getText();

        if (jTextFieldNombre.getText().equals("") || jTextFieldCodigo.getText().equals("")
                || jTextFieldPrecio.getText().equals("") || jTextFieldCosto.getText().equals("")
                || jTextFieldT6.getText().equals("") || jTextFieldT8.getText().equals("")
                || jTextFieldT10.getText().equals("") || jTextFieldT12.getText().equals("")
                || jTextFieldT14.getText().equals("") || jTextFieldT16.getText().equals("")
                || jTextFieldT18.getText().equals("") || jTextFieldT20.getText().equals("")
                || jTextFieldT22.getText().equals("")) {

            JOptionPane.showMessageDialog(this, "Debe ingresar toda la informaciÃ³n");

        } else {
            ep = pDAO.buscar(codigoP);
            if (ep.getCodigo() != null) {

                JOptionPane.showMessageDialog(this, "Un producto con este codigo ya fue registrado");

            } else {

                int precioP = Integer.parseInt(jTextFieldPrecio.getText());
                if (precioP < 0 || precioP > 100000) {

                    JOptionPane.showMessageDialog(this, "El precio no debe superar los 100.000$ y no debe ser negativo");
                    jTextFieldPrecio.requestFocus();

                } else {
                    int costoP = Integer.parseInt(jTextFieldCosto.getText());
                    if (costoP < 0 || costoP > 50000) {

                        JOptionPane.showMessageDialog(this, "El costo promedio del producto no debe superar los 50.000$ y no debe ser negativo");
                        jTextFieldCosto.requestFocus();

                    } else {

                        if (costoP >= precioP) {

                            JOptionPane.showMessageDialog(this, "El costo promedio del producto no debe superar al precio de venta");
                            jTextFieldCosto.requestFocus();
                        } else {
                            int T6 = Integer.parseInt(jTextFieldT6.getText());
                            int T8 = Integer.parseInt(jTextFieldT8.getText());
                            int T10 = Integer.parseInt(jTextFieldT10.getText());
                            int T12 = Integer.parseInt(jTextFieldT12.getText());
                            int T14 = Integer.parseInt(jTextFieldT14.getText());
                            int T16 = Integer.parseInt(jTextFieldT16.getText());
                            int T18 = Integer.parseInt(jTextFieldT18.getText());
                            int T20 = Integer.parseInt(jTextFieldT20.getText());
                            int T22 = Integer.parseInt(jTextFieldT22.getText());

                            if (T6 < 0 || T6 > 100 || T8 < 0 || T8 > 100 || T10 < 0 || T10 > 100
                                    || T12 < 0 || T12 > 100 || T14 < 0 || T14 > 100 || T16 < 0 || T16 > 100
                                    || T18 < 0 || T18 > 100 || T20 < 0 || T20 > 100 || T22 < 0 || T22 > 100) {
                                JOptionPane.showMessageDialog(this, "Las cantidades de los productos por talla no debe ser negativa  ni ser superior a 100");
                                jTextFieldCosto.requestFocus();
                            } else {
                                agregar();
                                JOptionPane.showMessageDialog(this, "Producto agregado");
                                limpiar();

                            }

                        }

                    }

                }

            }

        }


    }//GEN-LAST:event_jButtonAgregarUsuarioActionPerformed

