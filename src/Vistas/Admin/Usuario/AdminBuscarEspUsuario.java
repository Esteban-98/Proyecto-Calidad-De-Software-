    private void jTextFieldNitKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldNitKeyTyped
       
        char v = evt.getKeyChar();
        int c = (int) evt.getKeyChar();

        soloNumeros(evt, v, c);
        
    }//GEN-LAST:event_jTextFieldNitKeyTyped

      public void soloNumeros(java.awt.event.KeyEvent evt, char v, int c){
    

        if (Character.isLetter(v)){
        
            getToolkit().beep();
            evt.consume();
            
            JOptionPane.showMessageDialog(this, "Digita solo números");
            
        }
        if (Character.isSpaceChar(v)){
        
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(this, "No ingrese espacios en blanco");
            
        }
        if (c > 32 && c <= 47 || c >= 58 && c <= 64 || c >= 91 && c <= 96 || c >= 123 && c <= 208 || c >= 210 && c <= 240 || c >= 242 && c <=255){
        
            getToolkit().beep();
            evt.consume();
            
            JOptionPane.showMessageDialog(this, "No ingrese caracteres especiales");
            
        }
    
    
    }
    public void buscar(){
       
        String nit = jTextFieldNit.getText();
        
        if(jTextFieldNit.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Debe ingresar la identifición en la caja de texto");
            jTextFieldNit.requestFocus();
        }else{
            
            eu = uDAO.buscar(nit);
            if (eu.getNit() != null ){
                jTextFieldNombre1.setText(eu.getPrimerNombre());
                jTextFieldNombre2.setText(eu.getSegundoNombre());
                jTextFieldApellido1.setText(eu.getPrimerApellido());
                jTextFieldApellido2.setText(eu.getSegundoApellido());
                jTextFieldEdad.setText(String.valueOf(eu.getEdad()));
                jTextFieldDireccion.setText(eu.getDireccion());
                jTextFieldTelefono1.setText(eu.getTelefono1());
                jTextFieldTelefono2.setText(eu.getTelefono2());
                jTextFieldNomEmer.setText(eu.getContactoEmergencia());
                jTextFieldTelEmer.setText(eu.getTelefonoEmergencia());
            }else{
                 JOptionPane.showMessageDialog(this,"El número de identificación que ingreso no está registrado");
                 jTextFieldNit.requestFocus();
             }
        }       
       
    }


